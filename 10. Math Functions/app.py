import math
x = 2.9

# We can round x by built-in round function
# Round FUNCTION
print(round(x))  # 3

# abs FUNCTION -> absolute
# We give it a value and it always returns the positive representation of the value, even if value is negative
print(abs(-2.9))  # 2.9
# Absolute always returns a positive number


