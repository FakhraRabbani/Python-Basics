# Addition Operator
print(10 + 3)  # 13
# Subtraction Operator
print(10 - 3)  # 7
# Multiplication Operator
print(10 * 3)  # 30
# Two types of division operator
# Division Operator / -> We get float
print(10 / 3)  # 3.3333333333333335
# Division Operator // -> We get integer
print(10 // 3)  # 3
# Modulus Operator
print(10 % 3)  # 1
# Exponent Operator -> Power (Two asteriks) -> 10 ** 3 means 10 to power of 3
print(10 ** 3)  # 1000

# Increment A variable
x = 10
# Now we want to increment this by 3
x = x + 3
print(x)  # 13

# Augmented assigment operator -> Same increment code but in shorter form
y = 10
y += 3  # Same as y = y + 3
print(y)  # 13
z = 5
z *= 2  # Same as z = z * 2
print(z)  # 10
